    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        if (tblView.getSelectedRow() >= 0 && !txtId.getText().isEmpty()) {
            ChargeType chargeType = new ChargeType();
            chargeType.setTypeId(Integer.parseInt(txtId.getText()));
            chargeType.setTypeName(txtName.getText());
            chargeType.setTypeDesc(txtDesc.getText());
            chargeType.setAmount(Double.parseDouble(txtAmt.getText()));
            if (dbChargeType.delete(chargeType)) {
                int idx = tblView.getSelectedRow();
                chargeTypes.remove(idx);
                clear();
                refreshTable();
            }
        }
    }//GEN-LAST:event_btDeleteActionPerformed

