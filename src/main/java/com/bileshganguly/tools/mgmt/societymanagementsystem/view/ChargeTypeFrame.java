    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        if (validateFields()) {
            ChargeType chargeType = new ChargeType();
            chargeType.setTypeName(txtName.getText());
            chargeType.setTypeDesc(txtDesc.getText());
            chargeType.setAmount(Double.parseDouble(txtAmt.getText()));
            if (dbChargeType.insert(chargeType)) {
                chargeTypes.add(chargeType);
                clear();
                refreshTable();
            }
        } else {
            JOptionPane.showMessageDialog(this,
                    "Please check the values in the fields."
                    + "\nNone of the fields can be empty."
                    + "\nThe amount field only takes numbers.",
                    "Invalid Value in fields", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btAddActionPerformed

    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
        if (validateFields()) {
            ChargeType chargeType = new ChargeType();
            chargeType.setTypeId(Integer.parseInt(txtId.getText()));
            chargeType.setTypeName(txtName.getText());
            chargeType.setTypeDesc(txtDesc.getText());
            chargeType.setAmount(Double.parseDouble(txtAmt.getText()));
            if (dbChargeType.update(chargeType)) {
                int idx = tblView.getSelectedRow();
                chargeTypes.set(idx, chargeType);
                clear();
                refreshTable();
            }
        } else {
            JOptionPane.showMessageDialog(this,
                    "Please check the values in the fields."
                    + "\nNone of the fields can be empty."
                    + "\nThe amount field only takes numbers.",
                    "Invalid Value in fields", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btUpdateActionPerformed

