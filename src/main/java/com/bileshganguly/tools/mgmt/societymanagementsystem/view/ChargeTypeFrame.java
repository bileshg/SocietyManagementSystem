    private class ChargeTypeModel extends AbstractTableModel {

        private final List<ChargeType> contents;
        private final String[] columns;

        public ChargeTypeModel(List<ChargeType> contents, String[] columns) {
            this.contents = contents;
            this.columns = columns;
        }

        @Override
        public String getColumnName(int column) {
            return columns[column];
        }

        @Override
        public int getRowCount() {
            return this.contents.size();
        }

        @Override
        public int getColumnCount() {
            return 4;
        }

        @Override
        public Class<?> getColumnClass(int columnIndex) {
            switch (columnIndex) {
                case 0:
                    return Integer.class;
                case 3:
                    return Double.class;
                default:
                    return String.class;
            }
        }

        @Override
        public Object getValueAt(int row, int column) {
            ChargeType chargeType = contents.get(row);

            switch (column) {
                case 0:
                    return chargeType.getTypeId();
                case 1:
                    return chargeType.getTypeName();
                case 2:
                    return chargeType.getTypeDesc();
                case 3:
                    return chargeType.getAmount();
                default:
                    return "N/A";
            }
        }

    }

    private boolean validateFields() {
        return validateEmpty() && validateDouble();
    }

    private boolean validateEmpty() {
        return !txtName.getText().isEmpty()
                && !txtDesc.getText().isEmpty()
                && !txtAmt.getText().isEmpty();
    }

    private boolean validateDouble() {
        try {
            double d = Double.parseDouble(txtAmt.getText());
        } catch (NumberFormatException e) {
            return false;
        }
        return true;
    }

    private void init() {
        databaseConnection = new DatabaseConnection();
        if (databaseConnection.connect()) {
            dbChargeType = new ChargeTypeDaoImpl(databaseConnection);
            populateList();
        }

        tblView.getSelectionModel().addListSelectionListener((ListSelectionEvent e) -> {
            if (e.getValueIsAdjusting()) {
                return;
            }

            final DefaultListSelectionModel target = (DefaultListSelectionModel) e.getSource();
            int isSelectedIndex = target.getAnchorSelectionIndex();

            setFields(isSelectedIndex);
        });

        this.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent e) {
                super.windowClosing(e);
                databaseConnection.disconnect();
                if(parent != null) parent.setEnabled(true);
            }
        });
    }

    private void clear() {
        tblView.clearSelection();

        txtAmt.setText("");
        txtDesc.setText("");
        txtId.setText("");
        txtName.setText("");
    }

    private void setFields(int idx) {
        if (idx >= 0 && idx < chargeTypes.size()) {
            ChargeType chargeType = chargeTypes.get(idx);

            txtAmt.setText(String.format("%.2f", chargeType.getAmount()));
            txtDesc.setText(chargeType.getTypeDesc());
            txtId.setText(Integer.toString(chargeType.getTypeId()));
            txtName.setText(chargeType.getTypeName());
        }
    }

    private void populateList() {
        chargeTypes = dbChargeType.getAll();
        dataModel = new ChargeTypeModel(chargeTypes, fieldList);
        tblView.setModel(dataModel);
    }

    private void refreshTable() {
        dataModel.fireTableDataChanged();
    }
